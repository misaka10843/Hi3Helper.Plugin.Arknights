    public static string ReadConfig(string filePath)
    {
        if (!File.Exists(filePath)) return string.Empty;

        byte[] fileBytes;
        try 
        {
            fileBytes = File.ReadAllBytes(filePath);
        }
        catch (Exception) 
        {
            return string.Empty;
        }

        var length = fileBytes.Length;
        var decryptedBytes = new byte[length];

        for (var i = 0; i < length; i++)
        {
            var key = i < KeyTable.Length ? KeyTable[i] : (byte)0;
            decryptedBytes[i] = (byte)(fileBytes[i] ^ key);
        }
        
        var content = Encoding.UTF8.GetString(decryptedBytes);
        var startIndex = content.IndexOf('[');
        if (startIndex != -1)
        {
            content = content.Substring(startIndex);
        }
        
        var lastBrace = content.LastIndexOf('}');
        if (lastBrace != -1)
        {
            content = content.Substring(0, lastBrace + 1);
        }
        else
        {
            content = content.Trim();
        }

        return content;
    }

    public static string? ParseVersion(string content)
    {
        using var reader = new StringReader(content);
        string? line;
        while ((line = reader.ReadLine()) != null)
        {
            var trimmed = line.Trim();
            if (trimmed.StartsWith("version", StringComparison.OrdinalIgnoreCase)) 
            {
                var parts = trimmed.Split('=');
                if (parts.Length > 1)
                {
                    return parts[1].Trim();
                }
            }
        }
        return null;
    }
